// This function is executed upon complete reception of a CAN message.
void HAL_CAN_RxCpltCallback(CAN_HandleTypeDef* hcan)
{
	// Set ledState to the 1st byte of the tx'd data.
	ledState = hcan->pRxMsg->Data[0];
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, ledState);

	// Start listening for the next CAN message
	HAL_CAN_Receive_IT(&hcan1, CAN_FIFO0);
}

